#!/usr/bin/env python3
"""
This module contains a function to return the concatinated string of 2 strings
"""


def concat(str1: str, str2: str) -> str:
    """
    Returns the concatinated string of 2 strings

    Args:
        str1 (str): first string
        str2 (str): second string

    Returns:
        str: concatinated string of str1 and str2
    """
    return str1 + str2
