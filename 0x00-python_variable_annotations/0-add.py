#!/usr/bin/env python3
"""
This module contains a function to return the sum of 2 floats
"""


def add(a: float, b: float) -> float:
    """
    Returns the sum of 2 floats

    Args:
        a (float): first number
        b (float): second number

    Returns:
        float: sum of a and b
    """
    return a + b
